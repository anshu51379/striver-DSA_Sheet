class Solution
{
    public:
    //Function to count nodes of a linked list.
    int getCount(struct Node* head){
        //Code here
        int cnt=1;
        Node* tmp=head;
        if(tmp->next==NULL){
            return 0;
        }
        while(tmp->next!=NULL){
            cnt+=1;
            tmp=tmp->next;
        }
        return cnt;
    }
};
    


